#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	while (((char *)s)[i] != '\0')
	{
		if (((char *)s)[i] == (unsigned char) c)
			return ((char *)(s + i));
		i++;
	}
	if (!c || !((char *)s))
		return ((char *)(s + i));
	return (0);
}
